
/**
* Programa: Tipos de Datos
*
* El objetivo de este programa es utilizar de forma general
* los distintos tipos de datos en Java.
*
* @author: Mauricio Rodriguez
*/
public class TiposDeDatos {
	
	public static void main(String[] args){
		
		// tipo_de_dato nombre_variable = valor;
		
		// Srting
		String miString = "Hola Mundo!";
		
		// int
		int edad = 30;
		
		// short
		short numeroPuerta = 1140;
		
		// long
		long sumaFondosAnuales = 22563215;
		
		// float
		float estatura = 1.82f;
		
		// character
		char sexo = 'F';
		
		// boolean 
		boolean habilitado = false;
		
		System.out.println("String: " + miString);
		System.out.println("edad: " + edad);
		System.out.println("numeroPuerta: " + numeroPuerta);
		System.out.println("estatura: " + estatura);
		System.out.println("sexo: " + sexo);
		System.out.println("habilitado: " + habilitado);
		
	}
	
}