
/**
* Programa: Estructura While
*
* El objetivo de este programa es utilizar la estructura
* de control iterativa "While".
*
* @author: Mauricio Rodriguez
*/
public class EstructuraWhile {

	public static void main(String[] args){
		
		System.out.println("");
		System.out.println("######### Estructura de Control WHILE  #########");
		System.out.println("");
		
		System.out.println("Comenzando ejecucion del while...");
		int indiceWhile = 0;
		while(indiceWhile < 10){
			System.out.println("Valor indice: " + indiceWhile);
			indiceWhile++;
		}
		System.out.println("Finalizada ejecucion del while...");
		
	}
	

}