public class EstructuraFor {

	public static void main(String[] args){
		
		System.out.println("");
		System.out.println("######### Estructura de Control FOR  #########");
		System.out.println("");
		
		System.out.println("Comenzando ejecuci√≥n del for...");
		for (int indice = 0; indice < 11; indice++){
			System.out.println("ejecutando iteracion " + indice + "...");
		}
		System.out.println("Finalizada ejecucion del for...");
	}
	

}