
/**
* Programa: Condicional Doble
*
* El objetivo de este programa es utilizar la estructura
* de control selectiva Condicional Doble (if..else)
*
* @author: Mauricio Rodriguez
*/
public class CondicionalDoble {

	public static void main(String[] args){
		
		String usuario = "supervisor";
		
		if (usuario.equals("admin")){
			System.out.println("El usuario es administrador!");
		} else {
			System.out.println("El usuario no es administrador!");
		}
	
	}
	

}